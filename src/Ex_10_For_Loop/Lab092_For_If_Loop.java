package Ex_10_For_Loop;

public class Lab092_For_If_Loop {
    public static void main(String[] args) {
        for (int jennie=0; jennie < 18; jennie++){
            if (jennie > 15){
                System.out.println("Gift From Papa, Iphone");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
