package Ex004_Operatprs;

public class Lab034_Operator_Relational {
    public static void main(String[] args) {
//       < -> Less Than
//       <= -> Less than or Equal to
//       > -> Greater
//       >= -> Equal to
//       != -> Not Equal
// All of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a>b;
        System.out.println(c);

        int age_jennie = 21;
        int age_baba=51;
        boolean result= age_jennie>=age_baba;
        System.out.println(result);

    }
}
